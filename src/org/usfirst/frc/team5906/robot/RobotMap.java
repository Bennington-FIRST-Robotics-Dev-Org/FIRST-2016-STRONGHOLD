package org.usfirst.frc.team5906.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */ 

public class RobotMap {
	public static int DrivePort1 = 0; 
	public static int DrivePort2 = 1; 
	public static int DrivePort3 = 2; 
	public static int DrivePort4 = 3; 
	
	public static int CompressorID = 0; 
	public static int SolenoidForwardID = 0; 
	public static int SolenoidReverseID = 1; 
	public static int PressureSensorPort = 0; 
}

